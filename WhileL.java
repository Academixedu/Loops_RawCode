public class WhileL{
  public static void main(String[] args) {
    int i=1;
    while (i<7) {
      System.out.println(+i);
      i++;
    }
    int j=7;
    while (j>1) {
      System.out.println(+j);
      j--;
    }
  }
//1) Prepare a While Loop Using ++ operator
//2) Prepare a While Loop using -- operator
  // Number Should be reduced by 2 on every iteration
  
  
}
