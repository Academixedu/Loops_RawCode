public class DoWhile{
// Prepare a DoWhile Loop Using an Increment Operator 
  // Preapare a DoWhile Loop Using an Decrement Operator
  public static void main(String[] args) {
    int i=1;
    do{
      System.out.println("loop working :"+i);
      i++;
    }
    while(i<=5);

    System.out.println("decrement operator");

    // decrement operator
    int j=5;
    do{
      System.out.println("loop working :"+j);
      j--;
    }
    while(j>=1);
  }
}
