public class DoWhile{
  public static void main(String[] args) {
    // Prepare a DoWhile Loop Using an Increment Operator 
    int i=1;
    do {
      System.out.println("Number: " + i + ", Square: " + (i * i));
      i++; 
  } while (i <= 10);
  // Preapare a DoWhile Loop Using an Decrement Operator
System.out.println("now same in decrement :");
int j=10;
do {
  System.out.println("Number: " + j + ", Square: " + (j * j));
  j--; 
} while (j >0);


}

 
}
