public class ForL{
  public static void main(String[] args) {
    System.out.println("Using increment operator:");
    for (int i = 1; i <= 5; i++) {
        System.out.println("Incrementing: " + i);
    }
    System.out.println("\nUsing decrement operator:");
    for (int j = 5; j >= 1; j--) {
        System.out.println("Decrementing: " + j);
    }
}
  
}
