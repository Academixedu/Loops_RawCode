public class ForL{
  public static void main(String[] args) {
    // Prepare a For Loop Using an Increment Operator 
    for (int i = 1; i <=10; i++) {
      System.out.println("hello"+ i);
       }

  // Prepare a For Loop Using Decrement Operator
    
  for (int i = 10; i >=1; i--) {
    System.out.println("hello"+ i);
     }
  }

  
}
