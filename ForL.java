public class ForL{
// Prepare a For Loop Using an Increment Operator 
  // Prepare a For Loop Using Decrement Operator
  public static void main(String[] args) {
    for (int i=1;i<=5;i++){
      System.out.println("for loop working :" +i);

    }

    System.out.println("decrement operator :");


    for (int i=5;i>=1;i--){
      System.out.println("for loop working :" +i);

    }
  }
  
}
