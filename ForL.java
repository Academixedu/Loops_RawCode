public class ForL{
// Prepare a For Loop Using an Increment Operator 
  // Prepare a For Loop Using Decrement Operator
  public static void main(String[] args) {
      for(int i=1;i<=5;i++){
        System.out.println("helloo dude"+i);
      }
      for(int i=10;i>=5;i--){
        System.out.println("helloo dude"+i);
      }
  }
}
